void uint32tobcd(uint8_t *p,uint32_t velue,uint8_t len)
{
    uint8_t i;
    for(i = 0; i < len ; i++)
    {
        *(p+len-i-1) = velue%10;
        velue = velue/10;
    }    
}

uint8_t hex2bcd(uint8_t velue)
{
    uint8_t ret = 0;
    ret = velue%10;
    ret += (velue/10)<<4;
    return ret;
}

void u8to2bcd(unsigned char sval,unsigned char *dbuf)
{
    *dbuf++ = sval>>4;
    *dbuf = sval&0x0f;   
}

