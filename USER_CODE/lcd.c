//转换像素信息为字节
void Turn_QR_Code(uint8_t qr_byte[][27],uint8_t ppixel[][MAX_MODULESIZE],int Version,uint8_t times)
{
    uint8_t i,j,t,temp=0,n=0;
    uint8_t pixel_Size,pixel_Size_times,pixel_no=0;
    
    
    pixel_Size =  (Version - 1)*4 +21;
//    pixel_Size_times = pixel_Size*times;
    
    for(i=0;i<pixel_Size;i++)   //行
    {
        n = 0;
        pixel_no = 0;
        temp = 0;
        for(j=0;j<pixel_Size;j++) 
        {
            if(j==0)
            {
                temp &= 0<<(7-n%8);
                n++; 
            }                
            for(t=0;t<times;t++)
            {

                temp |= ppixel[i][j]<<(7-n%8);
                n++;
                if(n%8 == 0)
                {
                   qr_byte[i][pixel_no++] = ~temp;
                   temp = 0; 
                }            
            }
        }
        if(n%8 != 0)
        {
            uint8_t tail_no_change[] = {0,0xc0,0xe0,0xf0,0xf8,0xf4,0xf2,0xf1};
            qr_byte[i][pixel_no++] = (~temp) & tail_no_change[n%8];
            temp = 0; 
        }         
    }
}

//显示二维码
