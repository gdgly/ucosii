{
    uint8_t qr_byte[MAX_MODULESIZE][27] = {0};
    uint8_t i,j,times;
    uint8_t pixel_Size;
    
    pixel_Size =  (Version - 1)*4 +21;    
  
    if(pixel_Size<40)
        times = 2;    
    Turn_QR_Code(qr_byte,ppixel,Version,times);//转换像素信息为字节
    T6963_DispQR_Code(QrCODE_X,QrCODE_Y,Version,qr_byte,times);
}


