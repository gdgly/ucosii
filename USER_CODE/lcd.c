//数据反转然后 2位bcd码占用一个字节 分开1位bcd码占用一个字节
void bcdtwo2bcdone(unsigned char *sbuf,unsigned char *dbuf,unsigned int dlen)
{
    uint8_t i;
    unsigned char * dp;
    
    for(dp = sbuf,i = 0; i< dlen/2; i++)
    {
        *(dp +i) ^= *(dp+dlen-1-i);
        *(dp+dlen-1-i) ^= *(dp +i);
        *(dp +i) ^= *(dp+dlen-1-i);
    }
    for(dp = dbuf,i = 0; i < dlen ; i++)
    {
        *dp++ = *sbuf >> 4;
        *dp++ = *sbuf++ & 0x0f;
    }
}


